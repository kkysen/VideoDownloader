import HtmlWebpackPlugin = require("html-webpack-plugin");
import {HtmlPluginFactory} from "../../util/webpack/html";
import {production} from "./production";

export const htmlPlugin: HtmlPluginFactory = function({name, templateName = name, chunks}) {
    return new HtmlWebpackPlugin({
        inject: true,
        chunks: chunks || undefined,
        filename: `${name}.html`,
        template: `./src/html/${templateName}.html`,
        hash: false,
        cache: true,
        // favicon: "./src/img/favicon.ico",
        showErrors: !production,
        minify: production && {
            caseSensitive: false,
            collapseBooleanAttributes: true,
            collapseInlineTagWhitespace: true,
            collapseWhitespace: true,
            conservativeCollapse: false,
            customAttrAssign: [],
            // customAttrCollapse: undefined,
            customAttrSurround: [],
            // customEventAttributes: [],
            decodeEntities: true,
            html5: true,
            ignoreCustomComments: [],
            ignoreCustomFragments: [],
            includeAutoGeneratedTags: true,
            keepClosingSlash: false,
            maxLineLength: Number.MAX_SAFE_INTEGER,
            minifyCSS: true,
            minifyJS: true,
            minifyURLs: true,
            preserveLineBreaks: false,
            preventAttributesEscaping: false,
            processConditionalComments: true,
            processScripts: [],
            quoteCharacter: "\"",
            removeAttributeQuotes: true,
            removeComments: true,
            removeEmptyAttributes: true,
            removeEmptyElements: false,
            removeOptionalTags: true,
            removeRedundantAttributes: true,
            removeScriptTypeAttributes: true,
            removeStyleLinkTypeAttributes: true,
            removeTagWhitespace: false,
            sortAttributes: true,
            sortClassName: true,
            trimCustomFragments: true,
            useShortDoctype: true,
        },
    });
};